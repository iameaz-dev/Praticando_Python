nome = "Iasmim de Medeiros Azevedo"

print(nome[0]) #mostra apenas o 1º caractere
print(nome[:9]) #mostra até o 9º caractere 
print(nome[10:]) #mostra a partir do 10º caractere
print(nome[10:16]) #mostra do 10º ao 16º caractere
print(nome[10:16:2]) #10º ao 16º caractere, de 2 em 2 passos
print(nome[:]) #mostra a string inteira
print(nome[::-1]) #mostra os caracteres espelhados



